#include "ft_put.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		if (n == INT_MIN)
		{
			ft_putstr("2147483648");
			return ;
		}
		else
			n = -n;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	ft_putchar((n % 10) + '0');
}
